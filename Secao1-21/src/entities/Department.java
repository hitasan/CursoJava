package entities;

public class Department {

	private String name;

	// Metodos Contrutores
	public Department() {
	}

	public Department(String name) {
		this.name = name;
	}


	// Metodos Get/Set
	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}
	
}
